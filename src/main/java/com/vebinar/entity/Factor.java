package com.vebinar.entity;

public class Factor {

    private int idfactor;
    private String nameFactor;
    private String codFactor;
    private String lvlFactor;
    private String workingConditions;

    public int getIdfactor() {
        return idfactor;
    }

    public void setIdfactor(int idfactor) {
        this.idfactor = idfactor;
    }

    public String getNameFactor() {
        return nameFactor;
    }

    public void setNameFactor(String nameFactor) {
        this.nameFactor = nameFactor;
    }

    public String getCodFactor() {
        return codFactor;
    }

    public void setCodFactor(String codFactor) {
        this.codFactor = codFactor;
    }

    public String getLvlFactor() {
        return lvlFactor;
    }

    public void setLvlFactor(String lvlFactor) {
        this.lvlFactor = lvlFactor;
    }

    public String getWorkingConditions() {
        return workingConditions;
    }

    public void setWorkingConditions(String workingConditions) {
        this.workingConditions = workingConditions;
    }
}
